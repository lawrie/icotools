void send_cmd(uint8_t r, uint8_t d) {
	uint32_t status;

	icosoc_i2c_write(0x3c, 0x00, r, d);
	do {
		status = icosoc_i2c_status();
	} while ((status >> 31) != 0);
	printf("Status is %lx\n",icosoc_i2c_status());
}

void send_data(uint8_t d1, uint8_t d2) {
        uint32_t status;

        icosoc_i2c_write(0x3c, 0x40, d1, d2);
        do {
                status = icosoc_i2c_status();
        } while ((status >> 31) != 0);
        //printf("Status is %lx\n",icosoc_i2c_status());
}

